        ).openPopup();

        // Add pulse animation effect
        var pulseIcon = L.divIcon({
            className: 'pulse-icon',
            html: `<div style="position: relative; width: 40px; height: 40px;">
                     <div style="position: absolute; top: 10px; left: 10px; background-color: rgba(124, 163, 12, 0.3); width: 20px; height: 20px; border-radius: 50%; animation: pulse 1.5s infinite;"></div>
                   </div>`,
            iconSize: [40, 40],
            iconAnchor: [20, 20]
        });

        L.marker([-1.2198656, 36.8390024], {
            icon: pulseIcon
        }).addTo(map);

        // Add CSS animation
        var style = document.createElement('style');
        style.innerHTML = `
            @keyframes pulse {
                0% {
                    transform: scale(0.5);
                    opacity: 1;
