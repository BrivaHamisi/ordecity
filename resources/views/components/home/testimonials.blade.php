    <!-- JavaScript for Carousel -->
    <script>
        document.addEventListener('DOMContentLoaded', () => {
            const carouselSlide = document.querySelector('.carousel-slide');
            const cards = document.querySelectorAll('.card');
            const prevButton = document.querySelector('.prev-button');
            const nextButton = document.querySelector('.next-button');
            const dots = document.querySelectorAll('.carousel-dot');
            let currentIndex = 0;
            const cardsToShow = window.innerWidth >= 1024 ? 3 : window.innerWidth >= 640 ? 2 : 1;
            const totalSlides = Math.ceil(cards.length / cardsToShow);

            function updateCarousel() {
                const offset = -currentIndex * (100 / cardsToShow);
                carouselSlide.style.transform = `translateX(${offset}%)`;
                dots.forEach((dot, index) => {
                    dot.classList.toggle('bg-[#0575E6]', index === currentIndex);
                    dot.classList.toggle('bg-[#0575E6]/30', index !== currentIndex);
                });
            }

            function moveToSlide(index) {
                currentIndex = index;
                if (currentIndex >= totalSlides) currentIndex = 0;
                if (currentIndex < 0) currentIndex = totalSlides - 1;
                updateCarousel();
            }

            nextButton.addEventListener('click', () => moveToSlide(currentIndex + 1));
            prevButton.addEventListener('click', () => moveToSlide(currentIndex - 1));
            dots.forEach((dot, index) => {
                dot.addEventListener('click', () => moveToSlide(index));
            });

            // Auto-slide every 5 seconds
            let autoSlide = setInterval(() => moveToSlide(currentIndex + 1), 5000);

            // Pause auto-slide on hover
            carouselSlide.addEventListener('mouseenter', () => clearInterval(autoSlide));
            carouselSlide.addEventListener('mouseleave', () => {
                autoSlide = setInterval(() => moveToSlide(currentIndex + 1), 5000);
            });

            // Handle window resize
            window.addEventListener('resize', () => {
                const newCardsToShow = window.innerWidth >= 1024 ? 3 : window.innerWidth >= 640 ? 2 : 1;
                if (newCardsToShow !== cardsToShow) {
                    updateCarousel();
                }
            });

            updateCarousel();
        });
    </script>
